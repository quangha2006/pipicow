def WeekFromInt(dayinweek):
    if dayinweek == 0:
        return "Mon"
    elif dayinweek == 1:
        return "Tue"
    elif dayinweek == 2:
        return "Web"
    elif dayinweek == 3:
        return "Thu"
    elif dayinweek == 4:
        return "Fri"
    elif dayinweek == 5:
        return "Sat"
    elif dayinweek == 6:
        return "Sun"
    return ""
        
